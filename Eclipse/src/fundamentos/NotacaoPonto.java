package fundamentos;

public class NotacaoPonto {
	
	public static void main(String[] args) {
		
		
		String s = "Bom dia X";
		System.out.println(s.toUpperCase());
		System.out.println(s.replaceAll("X", "Senhora"));
		
		
		System.out.println(s.concat(" Allan")
							.replace("X", ","));
		
		
		//Tipos primitivos nao tem a notacao ponto!!!
		
		
	}
}
